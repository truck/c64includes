	;; VIC II Register locations
	
	VIC = $d000
	SPR0XPOS = $D000	; 53248/VIC+0        Sprite 0 X Pos
	SPR0YPOS = $D001	; 53249/VIC+1        Sprite 0 Y Pos
	SPR1XPOS = $D002	; 53250/VIC+2        Sprite 1 X Pos
	SPR1YPOS = $D003	; 53251/VIC+3        Sprite 1 Y Pos
	SPR2XPOS = $D004	; 53252/VIC+4        Sprite 2 X Pos
	SPR2YPOS = $D005	; 53253/VIC+5        Sprite 2 Y Pos
	SPR3XPOS = $D006	; 53254/VIC+6        Sprite 3 X Pos
	SPR3YPOS = $D007	; 53255/VIC+7        Sprite 3 Y Pos
	SPR4XPOS = $D008	; 53256/VIC+8        Sprite 4 X Pos
	SPR4YPOS = $D009	; 53257/VIC+9        Sprite 4 Y Pos
	SPR5XPOS = $D00A	; 53258/VIC+10       Sprite 5 X Pos
	SPR5YPOS = $D00B	; 53259/VIC+11       Sprite 5 Y Pos
	SPR6XPOS = $D00C	; 53260/VIC+12       Sprite 6 X Pos
	SPR6YPOS = $D00D	; 53261/VIC+13       Sprite 6 Y Pos
	SPR7XPOS = $D00E	; 53262/VIC+14       Sprite 7 X Pos
	SPR7YPOS = $D00F	; 53263/VIC+15       Sprite 7 Y Pos
	SPRMSB = $D010		; 53264/VIC+16       Sprites 0-7 MSB of X coordinate
	VICCTL1 = $D011		; 53265/VIC+17       Control Register 1
	RASPOS = $D012		; 53266/VIC+18       Raster Position
	LATCHX = $D013		; 53267/VIC+19       Latch X Pos
	LATCHY = $D014		; 53268/VIC+20       Latch Y Pos
	SPRDSPE = $D015		; 53269/VIC+21       Sprite display Enable
	VICCTL2 = $D016		; 53270/VIC+22       Control Register 2
	SPR2XPV = $D017		; 53271/VIC+23       Sprites Expand 2x Vertical (Y)
	MEMCTL = $D018		; 53272/VIC+24       Memory Control Register
	VICIRR = $D019		; 53273/VIC+25       Interrupt Request Register (IRR)
	INTREQ = $D019		; - I always call it intreq anyway
	VICIMR = $D01A		; 53274/VIC+26       Interrupt Mask Register (IMR)
	SPRBKG = $D01B		; 53275/VIC+27       Sprite to Background Display Priority
	SPRMCM = $D01C		; 53276/VIC+28       Sprites Multi-Color Mode Select
	SPR2XPH = $D01D		; 53277/VIC+29       Sprites Expand 2x Horizontal (X)
	SPRSPRCD = $D01E	; 53278/VIC+30       Sprite to Sprite Collision Detect
	SPRBKGCD = $D01F	; 53279/VIC+31       Sprite to Background Collision Detect
	BDRCOL = $D020		; 53280/VIC+32       Border Color
	BKGCOL0 = $D021		; 53281/VIC+33       Background Color 0
	BKGCOL1 = $D022		; 53282/VIC+34       Background Color 1, Multi-Color Register 0
	BKGCOL2 = $D023		; 53283/VIC+35       Background Color 2, Multi-Color Register 1
	BKGCOL3 = $D024		; 53284/VIC+36       Background Color 3
	SPRMCR0 = $D025		; 53285/VIC+37       Sprite Multi-Color Register 0
	SPRMCR1 = $D026		; 53286/VIC+38       Sprite Multi-Color Register 1
	SPR0COL = $D027		; 53287/VIC+39       Sprite 0 Color
	SPR1COL = $D028		; 53288/VIC+40       Sprite 1 Color
	SPR2COL = $D029		; 53289/VIC+41       Sprite 2 Color
	SPR3COL = $D02A		; 53290/VIC+42       Sprite 3 Color
	SPR4COL = $D02B		; 53291/VIC+43       Sprite 4 Color
	SPR5COL = $D02C		; 53292/VIC+44       Sprite 5 Color
	SPR6COL = $D02D		; 53293/VIC+45       Sprite 6 Color
	SPR7COL = $D02E		; 53294/VIC+46       Sprite 7 Color
